--5). PERCENTAGE OF CUSTOMER WHO PLACED MORE THAN 1 ORDER.

WITH CUSTOMER_TYPE AS (
    SELECT 
        C.CUSTOMER_UNIQUE_ID,
        COUNT(DISTINCT F.ORDER_KEY) AS TOTAL_ORDERS,
        SUM(F.TOTAL_AMOUNT) AS TOTAL_REVENUE
    FROM CRM_FACT_TABLE F
    JOIN DIM_CRM_CUSTOMERS C
        ON F.CUSTOMER_KEY = C.CUSTOMER_KEY
    GROUP BY C.CUSTOMER_UNIQUE_ID
),
CALCULATING_REPEAT_CUSTOMER AS (
					SELECT 
					    CASE 
					        WHEN TOTAL_ORDERS > 1 THEN 'Repeat'
					        ELSE 'One-Time'
					    END AS CUSTOMER_TYPE,
					    COUNT(*) AS CUSTOMER_COUNT,
					    ROUND(AVG(TOTAL_REVENUE),2) AS AVG_REVENUE
					FROM CUSTOMER_TYPE
					GROUP BY CUSTOMER_TYPE
					),
REPEAT_SELECTION AS (
					SELECT *,CUSTOMER_COUNT AS REPEATS
					FROM CALCULATING_REPEAT_CUSTOMER
					WHERE CUSTOMER_TYPE='Repeat'
					
					
)
SELECT R.CUSTOMER_TYPE,ROUND(R.CUSTOMER_COUNT/SUM(C.CUSTOMER_COUNT) * 100,2) AS PERCENTAGE_OF_REPEAT_CUSTOMERS 
FROM CALCULATING_REPEAT_CUSTOMER C
CROSS JOIN
REPEAT_SELECTION R
GROUP BY R.CUSTOMER_TYPE,R.CUSTOMER_COUNT;


