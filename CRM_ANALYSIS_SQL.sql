--****************************** RFM MODELING ***********************************
WITH RFM_BASE AS (SELECT
					 C.CUSTOMER_UNIQUE_ID AS UNIQUE_ID,
					 MAX(O.PURCHASE_DATE) AS LAST_PURCHASE_DATE,
					 COUNT(DISTINCT O.ORDER_ID) AS FREQUENCY,
					 COALESCE(SUM(F.TOTAL_AMOUNT),0) AS MONETARY
					 FROM
					 CRM_FACT_TABLE F
					 JOIN
					 DIM_CRM_ORDER_DETAILS O
					 ON
					 F.ORDER_KEY=O.ORDER_KEY
					 JOIN
					 DIM_CRM_CUSTOMERS C
					 ON
					 F.CUSTOMER_KEY=C.CUSTOMER_KEY
					 WHERE
					 O.ORDER_STATUS = 'delivered' and 
					 C.CUSTOMER_UNIQUE_ID IS NOT NULL
					 GROUP BY
					 C.CUSTOMER_UNIQUE_ID
					 
						),
RFM_CALC AS (
    SELECT *,
        EXTRACT(DAY FROM CURRENT_DATE - LAST_PURCHASE_DATE) AS RECENCY
    FROM RFM_BASE
),

RFM_SCORE AS (
    SELECT *,
        NTILE(5) OVER (ORDER BY RECENCY ASC) AS R_SCORE,
        NTILE(5) OVER (ORDER BY FREQUENCY DESC) AS F_SCORE,
        NTILE(5) OVER (ORDER BY MONETARY DESC) AS M_SCORE
    FROM RFM_CALC
)

SELECT *,
CASE 
    WHEN R_SCORE = 5 AND F_SCORE = 5 AND M_SCORE = 5 THEN 'Champions'
    WHEN R_SCORE >= 4 AND F_SCORE >= 4 THEN 'Loyal Customers'
    WHEN R_SCORE >= 4 AND F_SCORE <= 2 THEN 'New Customers'
    WHEN R_SCORE <= 2 AND F_SCORE >= 4 THEN 'At Risk'
    WHEN R_SCORE = 1 AND F_SCORE = 1 THEN 'Lost Customers'
    ELSE 'Potential'
END AS CUSTOMER_SEGMENT
FROM RFM_SCORE;


