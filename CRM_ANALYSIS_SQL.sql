WITH MONTHLY_REVENUE AS (
				SELECT D.YEAR AS YEARS, D.MONTH AS MONTHS,D.MONTH_NAME AS MONTH_NAME,
				ROUND(SUM(F.TOTAL_AMOUNT),2) AS AMOUNT,
				RANK() OVER (PARTITION BY D.YEAR ORDER BY SUM(F.TOTAL_AMOUNT) DESC) AS RANKS 
				FROM CRM_FACT_TABLE F
				JOIN
				DIM_CRM_DATE D
				ON
				F.DATE_KEY=D.DATE_KEY
				GROUP BY D.YEAR,D.MONTH,D.MONTH_NAME
				
)

SELECT YEARS,MONTHS,MONTH_NAME,AMOUNT
FROM MONTHLY_REVENUE
WHERE 
RANKS <4;

